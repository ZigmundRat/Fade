class CharacterClassifier
{
    static const char _classifications[];

    static const int DigitBit = 1;
    static const int AlphaBit = 2;
    static const int UnderscoreBit = 4;
    static const int DotBit = 8;

public:
    static bool IsDigit(char c)
    {
        return _classifications[(int)c] & DigitBit;
    }

    static bool IsAlpha(char c)
    {
        return (_classifications[(int)c] & AlphaBit) != 0;
    }

    static bool IsIdentifier(char c)
    {
      return (_classifications[(int)c] & (AlphaBit | DigitBit | UnderscoreBit)) != 0;
    }

    static bool IsNumber(char c)
    {
        return (_classifications[(int)c] & (DigitBit | DotBit)) != 0;
    }
};

